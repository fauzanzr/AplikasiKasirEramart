    private void jTable_barangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_barangMouseClicked
        // TODO add your handling code here:
        
        st_edit = true;
        int baris = jTable_barang.getSelectedRow();
        
        idbarang = Integer.parseInt(jTable_barang.getModel().getValueAt(baris, 0).toString() );
        
        //ambil data berdasarkan id
        sql = "SELECT * FROM barang WHERE idbarang = "+idbarang;
        
        try {
            ResultSet rs = koneksi.Database.read(sql);
            
            while( rs.next() ){
                jTextField_nama.setText( rs.getString("namabarang"));
                jTextField_harga.setText( rs.getString("harga"));
                jTextField_stok.setText(rs.getString("stok"));
                
                String tanggal = jTable_barang.getValueAt(baris,4).toString();
                Date convert = null;
                try{
                    convert = new SimpleDateFormat("yyyy-MM-dd").parse(tanggal);   
                }catch(Exception e){
                    System.out.println(e);
                }
                jDateChooser1.setDate(convert);
                
                idkategori = rs.getInt("idkategori");
                
                for( int i = 0; i < arrKategori.size(); i++)
                    if ( idkategori == arrKategori.get(i).getIdKategori() )
                        jComboBox_kategori.setSelectedIndex(i);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog( null, ex.toString() );
        }
    }//GEN-LAST:event_jTable_barangMouseClicked

    private void jButton_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addActionPerformed
        // TODO add your handling code here:
        
        SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal2 = date.format(jDateChooser1.getDate());
        
        if ( st_edit == false ){ //simpan
            sql = "INSERT INTO barang( idkategori, namabarang, harga, stok, tanggal) "
                    + "VALUES( "+ idkategori +", "
                    + "'"+ jTextField_nama.getText().trim()  +"', "
                    + "'"+ jTextField_harga.getText().trim() +"', "
                    + "'"+ jTextField_stok.getText().trim()  +"', "
                    + "'"+ tanggal2 +"' )";
        }else{ //update / perbarui
            sql = "UPDATE barang SET idkategori = "+ idkategori +", "
                    + "namabarang = '"+ jTextField_nama.getText().trim() +"', "
                    + "harga = '"+ jTextField_harga.getText().trim() +"', "
                    + "stok = '"+ jTextField_stok.getText().trim() +"', "
                    + "tanggal = '"+ tanggal2 +"' "
                    + "WHERE idbarang = "+ idbarang;
        }
        boolean berhasil = false;
        
        try {
            berhasil = koneksi.Database.exec(sql);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog( null, ex );
        }
        
        if ( berhasil ){
            clear();
            showData( null );
            st_edit = false;
            idbarang = 0;
        }else{
            JOptionPane.showMessageDialog(null, "Gagal Eksekusi Data");
        }
    }//GEN-LAST:event_jButton_addActionPerformed

