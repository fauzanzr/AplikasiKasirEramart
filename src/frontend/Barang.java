    private void showData( String cari ){
        if ( cari == null ){
            sql = "SELECT "
                    + "idbarang AS ID, "
                    + "namabarang AS NAMA, "
                    + "harga AS HARGA, "
                    + "stok AS STOK, "
                    + "tanggal AS DATE, "
                    + "namakategori AS KATEGORI "
                    + "FROM view_kategoribarang ";
        }else{
            sql = "SELECT "
                    + "idbarang AS ID, "
                    + "namabarang AS NAMA, "
                    + "harga AS HARGA, "
                    + "stok AS STOK, "
                    + "tanggal AS DATE,"
                    + "namakategori AS KATEGORI "
                    + "FROM view_kategoribarang "
                    + "WHERE namabarang LIKE '%"+ cari +"%' ";
        }
        
        try {
            //MENAMPILKAN DATA PADA TABEL
            ResultSet rs = koneksi.Database.read(sql);
            jTable_barang.setModel( DbUtils.resultSetToTableModel(rs));
            koneksi.Database.closeDB();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog( null, e);
        }
    }
    
    private void loadKategori(){
        jComboBox_kategori.removeAllItems();
        try {
            ResultSet rs = koneksi.Database.read( "SELECT idkategori, namakategori FROM kategori " );
            
            while( rs.next() ){
                arrKategori.add( new Kategori( Integer.parseInt ( rs.getString("idkategori") ),rs.getString("namakategori")));
            }
            
            //MENGAMBIL DATA DARI CLASS KATEGORI UNTUK DITAMPILKAN DI COMBOBOX
            for( int i = 0; i < arrKategori.size(); i++ ){
                jComboBox_kategori.addItem( arrKategori.get( i ).getNamaKategori());
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }
    
